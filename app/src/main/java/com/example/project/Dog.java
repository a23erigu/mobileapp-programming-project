package com.example.project;

public class Dog {

    String name;
    int size;

    public Dog (String name){
        this.name = name;
    }

    public String getName() {
        return name;
    }
    public int getSize() {
        return size;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSize(int size) {
        this.size = size;
    }


}
